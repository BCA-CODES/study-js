const days = "saturday"

switch (days) {
    case "sunday":
        console.log("Sunday");
        break;
    case "monday":
        console.log("Monday");
        break;
    case "tuesday":
        console.log("Tuesday");
        break;
    case "wednesday":
        console.log("Wednesday");
        break;
    case "thrusday":
        console.log("Thrusday");
        break;
    case "friday":
        console.log("Friday");
        break;
    case "saturday":
        console.log("Saturday");
        break;

    default:
        console.log("Invalid day");
        break;
}